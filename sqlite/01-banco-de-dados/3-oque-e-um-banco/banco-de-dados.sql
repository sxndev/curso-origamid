    Banco de Dados:

    DBMS (Database Management System) é um software que permite criar, gerenciar e manipular bancos de dados. Ele fornece uma interface para os usuários interagirem com os dados, permitindo a criação, leitura, atualização e exclusão de informações.

    RDBMS (Relational Database Management System) é um tipo específico de DBMS que organiza os dados em tabelas relacionadas entre si. Ele utiliza a linguagem SQL (Structured Query Language) para manipular os dados e estabelecer relações entre as tabelas.

    SQL (Structured Query Language) é a linguagem padrão para interagir com bancos de dados relacionais. Ela permite criar, modificar e consultar bancos de dados, além de definir a estrutura das tabelas e as relações entre elas.

